package com.company.op;

public class ArithOp {
    public static void main(String[] args) {
        int number1 = 9;
        int number2 = 4;

        System.out.println("number1 + number2 = " + (number1 + number2)); // number1 + number2 = 13
        System.out.println("number1 - number2 = " + (number1 - number2)); // number1 - number2 = 5
        System.out.println("number1 * number2 = " + (number1 * number2)); // number1 * number2 = 36
        System.out.println("number1 / number2 = " + (number1 / number2)); // number1 / number2 = 2
        System.out.println("number1 & number2 = " + (number1 % number2)); // number1 & number2 = 1
    }
}
